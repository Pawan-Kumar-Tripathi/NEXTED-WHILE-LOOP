import java.io.*;
import java.util.*;
class NEXTWHILE
    {
        public void main()
        {
            int i =0;
            while(i<3)
            {
                int j = 0 ;
                while(j<5)
                {
                    System.out.print(j);
                    j++;
                }
                System.out.println(" ");
                i++;
            }
        }
    }